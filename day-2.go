func moveFromString(value string) Move {
	switch {
	case value == rock.name || value == rock.strategy:
		return rock
	case value == paper.name || value == paper.strategy:
		return paper
	case value == scissors.name || value == scissors.strategy:
		return scissors
	}
	panic("error parsing move")
}

func strategyFromString(move Move, value string) Move {
	switch {
	case value == "X":
		// lose
		switch {
		case move == rock:
			return scissors
		case move == paper:
			return rock
		case move == scissors:
			return paper

		}
	case value == "Y":
		// draw
		return move
	case value == "Z":
		// win
		switch {
		case move == rock:
			return paper
		case move == paper:
			return scissors
		case move == scissors:
			return rock
		}
	}
	panic("error handling move")
}

func calculateScore(move, strategy Move) int {
	return result(move, strategy) + strategy.score
}

func result(move, strategy Move) int {
	// paper beats rock
	// rock beats scissors
	// scissors beats paper
	if move == rock {
		if strategy == scissors {
			return 0
		}
		if strategy == paper {
			return 6
		}
		if strategy == rock {
			return 3
		}
	}
	if move == paper {
		if strategy == scissors {
			return 6
		}
		if strategy == paper {
			return 3
		}
		if strategy == rock {
			return 0
		}
	}

	if move == scissors {
		if strategy == scissors {
			return 3
		}
		if strategy == paper {
			return 0
		}
		if strategy == rock {
			return 6
		}
	}

	return 0
}
